// Lets us access our ASM functions from our C code.
extern "C" void idt_flush(idt_ptr_t*);

idt_entry_t idt_entries[IDT_ENTRIES];
idt_ptr_t   idt_ptr;

void set_idt_gate(int n, uint32_t handler) {
    idt_entries[n].low_offset = low_16(handler);
    idt_entries[n].sel = 16;        //Checked in the bootlader, code seg is 16
    idt_entries[n].always0 = 0;
    idt_entries[n].flags = 0x8E; 
    idt_entries[n].high_offset = high_16(handler);
}

